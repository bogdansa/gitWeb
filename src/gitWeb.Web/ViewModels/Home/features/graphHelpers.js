export function pickupColor(index) {

    if (index == 1) {
        return "black";
    } else if (index == 2) {
        return "yellow";
    } else if (index == 3) {
        return "green";
    } else if (index == 4) {
        return "red";
    } else if (index == 5) {
        return "#343453";
    }

    return "blue";

}
